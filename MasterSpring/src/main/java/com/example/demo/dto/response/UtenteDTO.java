package com.example.demo.dto.response;

public class UtenteDTO {
	
	private String email;
	private long id;
	
	public UtenteDTO() {
		
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
	
	
}
